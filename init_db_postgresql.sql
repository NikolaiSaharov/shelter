create or replace function trg_check_user_role_for_application()
returns trigger
language plpgsql
as $$
begin
  if exists (
      select 1
      from users u
      join roles r on r.roleid = u.roleid
      where u.userid = new.userid and r.rolename in ('Manager', 'Admin')
  ) then
    raise exception 'Managers and Admins cannot create adoption applications';
  end if;
  return new;
end;
$$;

drop trigger if exists trg_checkuserroleforapplication on applications;
create trigger trg_checkuserroleforapplication
before insert on applications
for each row execute function trg_check_user_role_for_application();

create or replace function trg_notify_manager_on_application()
returns trigger
language plpgsql
as $$
declare
  v_manager_id int;
begin
  select u.userid
    into v_manager_id
  from users u
  join roles r on r.roleid = u.roleid
  where r.rolename = 'Manager'
  order by u.userid
  limit 1;

  if v_manager_id is not null then
    insert into managernotifications(userid, message)
    values (v_manager_id, 'Получена новая заявка на усыновление для AnimalID ' || new.animalid::text);
  end if;
  return new;
end;
$$;

drop trigger if exists trg_notifymanageronapplication on applications;
create trigger trg_notifymanageronapplication
after insert on applications
for each row execute function trg_notify_manager_on_application();

