    meetingtitle varchar(200) not null check (btrim(meetingtitle) <> ''),
    meetingdescription varchar(500),
    meetinglink varchar(500) not null check (btrim(meetinglink) <> ''),
    scheduleddatetime timestamp not null,
    meetingduration int not null check (meetingduration > 0),
    createdbyuserid int not null references users(userid),
    createddatetime timestamp not null default now(),
    lastmodifieddatetime timestamp not null default now()
);

create table if not exists meetingparticipants (
    participantid int generated by default as identity primary key,
    meetingid int not null references videomeetings(meetingid) on delete cascade,
    userid int not null references users(userid),
    participantrole varchar(20) not null check (participantrole in ('Host', 'Attendee')),
    unique (meetingid, userid)
);

