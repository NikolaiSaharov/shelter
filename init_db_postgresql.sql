create or replace view vw_meetingdetails as
select
    vm.meetingid,
    vm.meetingtitle,
    vm.meetingdescription,
    vm.meetinglink,
    vm.scheduleddatetime,
    vm.meetingduration,
    ms.statusname as meetingstatus,
    a.applicationid,
    (u_app.lastname || ' ' || u_app.firstname) as applicantname,
    anim.animalname,
    (u_creator.lastname || ' ' || u_creator.firstname) as createdbyname,
    vm.createddatetime
from videomeetings vm
join meetingstatuses ms on vm.meetingstatusid = ms.meetingstatusid
join applications a on vm.applicationid = a.applicationid
join users u_app on a.userid = u_app.userid
join animals anim on a.animalid = anim.animalid
join users u_creator on vm.createdbyuserid = u_creator.userid;

create or replace view vw_meetingparticipants as
select
    mp.participantid,
    mp.meetingid,
    vm.meetingtitle,
    u.userid,
    (u.lastname || ' ' || u.firstname) as participantname,
    u.email,
    mp.participantrole
from meetingparticipants mp
join videomeetings vm on mp.meetingid = vm.meetingid
join users u on mp.userid = u.userid;

create or replace view vw_animalcatalog as
select
    a.animalid,
    a.animalname,
    a.age,
    a.gender,
    a.vaccinated,
    a.height,
    a.animalweight,
    s.statusname as status,
    b.breedname,
    t.typename,
    ac.charactername,
    a.imagepath
from animals a
join breeds b on a.breedid = b.breedid
join animaltypes t on b.typeid = t.typeid
join animalstatuses s on a.statusid = s.statusid
left join animalcharacters ac on a.characterid = ac.characterid
where s.statusname = 'Доступен';

create or replace view vw_applicationreport as
select
    app.applicationid,
    ast.statusname as status,
    app.applicationdate,
    (u.firstname || ' ' || u.lastname) as username,
    r.rolename,
    a.animalname as animalname
from applications app
join applicationstatuses ast on app.statusid = ast.statusid
join users u on app.userid = u.userid
join roles r on u.roleid = r.roleid
join animals a on app.animalid = a.animalid;

create or replace view vw_animalvaccinations as
select
    a.animalid,
    a.animalname,
    vt.vaccinationname,
    vr.vaccinationdate
from animals a
join animalvaccinations av on a.animalid = av.animalid
join vaccinationtypes vt on av.vaccinationtypeid = vt.vaccinationtypeid
left join vaccinationrecords vr on av.animalvaccinationid = vr.animalvaccinationid;

create or replace view vw_breedstats as
select
    b.breedname,
    count(a.animalid) as animalcount
from animals a
join breeds b on a.breedid = b.breedid
group by b.breedname;

create or replace view vw_latestnews as
select
    n.newsid,
    n.title,
    n.content,
    n.postdate,
    (u.firstname || ' ' || u.lastname) as authorname,
    r.rolename
from news n
join users u on n.userid = u.userid
join roles r on u.roleid = r.roleid
where n.ispublished = true
order by n.postdate desc
limit 5;

