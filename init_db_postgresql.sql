-- PostgreSQL bootstrap schema for AnimalShelterDB
-- Converted from MSSQL script to PostgreSQL (initial migration step)
-- Target DB: animalshelterdb

-- =========================================================
-- Core dictionaries
-- =========================================================

create table if not exists animalstatuses (
    statusid int generated by default as identity primary key,
    statusname varchar(20) not null unique,
    constraint ck_animalstatuses_statusname_ru
        check (statusname in ('Доступен', 'Пристроен', 'На рассмотрении'))
);

create table if not exists roles (
    roleid int generated by default as identity primary key,
    rolename varchar(20) not null unique,
    constraint ck_roles_rolename
        check (rolename in ('Guest', 'Manager', 'Admin'))
);

create table if not exists animaltypes (
    typeid int generated by default as identity primary key,
    typename varchar(50) not null unique,
    constraint ck_animaltypes_typename_not_empty check (btrim(typename) <> '')
);

create table if not exists breeds (
    breedid int generated by default as identity primary key,
    breedname varchar(100) not null unique,
    typeid int not null references animaltypes(typeid) on delete cascade,
    constraint ck_breeds_breedname_not_empty check (btrim(breedname) <> '')
);

create table if not exists animalcharacters (
    characterid int generated by default as identity primary key,
    charactername varchar(50) not null unique,
    description varchar(255),
    constraint ck_animalcharacters_name_not_empty check (btrim(charactername) <> '')
);

create table if not exists shelters (
    shelterid int generated by default as identity primary key,
    sheltername varchar(100) not null unique,
    address varchar(255),
    phone varchar(20),
    email varchar(100),
    description text,
    isactive boolean not null default true,
    createddate timestamp not null default now(),
    constraint ck_shelters_name_not_empty check (btrim(sheltername) <> '')
);

-- =========================================================
-- Users and profiles
-- =========================================================

create table if not exists users (
    userid int generated by default as identity primary key,
    email varchar(100) not null unique,
    passwordhash varchar(255) not null,
    lastname varchar(50) not null,
    firstname varchar(50) not null,
    middlename varchar(50),
    phone varchar(20) not null,
    registrationdate timestamp not null default now(),
    roleid int not null references roles(roleid) on delete no action,
    shelterid int references shelters(shelterid),
    constraint ck_users_email_like check (position('@' in email) > 1),
    constraint ck_users_passwordhash_not_empty check (btrim(passwordhash) <> ''),
    constraint ck_users_lastname_not_empty check (btrim(lastname) <> ''),
    constraint ck_users_firstname_not_empty check (btrim(firstname) <> ''),
    constraint ck_users_phone_plus check (phone ~ '^\+[0-9].*')
);

create table if not exists userprofiles (
    profileid int generated by default as identity primary key,
    userid int not null unique references users(userid) on delete cascade,
    homeaddress varchar(255),
    dateofbirth date check (dateofbirth <= current_date),
    profilepicture varchar(255),
    createddate timestamp not null default now(),
    updateddate timestamp not null default now()
);

-- =========================================================
-- Animals
-- =========================================================

create table if not exists animals (
    animalid int generated by default as identity primary key,
    animalname varchar(50) not null,
    age int not null check (age >= 0),
    gender varchar(10) not null check (gender in ('Male', 'Female', 'Unknown')),
    vaccinated boolean not null default false,
    description text,
    imagepath varchar(255),
    statusid int not null references animalstatuses(statusid),
    breedid int not null references breeds(breedid) on delete cascade,
    characterid int references animalcharacters(characterid),
    height numeric(5,2) check (height > 0),
    animalweight numeric(5,2) check (animalweight > 0),
    admissiondate date,
    shelterid int references shelters(shelterid),
    constraint ck_animals_name_not_empty check (btrim(animalname) <> '')
);

create table if not exists animalmedia (
    mediaid int generated by default as identity primary key,
    animalid int not null references animals(animalid) on delete cascade,
    mediatype varchar(10) not null check (mediatype in ('Photo', 'Video')),
    mediapath varchar(500) not null,
    displayorder int not null default 0,
    isprimary boolean not null default false,
    createddate timestamp not null default now()
);

create index if not exists ix_animalmedia_animalid on animalmedia(animalid);
create index if not exists ix_animalmedia_displayorder on animalmedia(animalid, displayorder);

-- =========================================================
-- Applications
-- =========================================================

create table if not exists applicationstatuses (
    statusid int generated by default as identity primary key,
    statusname varchar(20) not null unique
        check (statusname in ('Pending', 'Approved', 'Rejected'))
);

create table if not exists applications (
    applicationid int generated by default as identity primary key,
    userid int not null references users(userid) on delete cascade,
    animalid int not null references animals(animalid) on delete cascade,
    applicationdate timestamp not null default now(),
    statusid int not null references applicationstatuses(statusid),
    reason varchar(500) not null check (btrim(reason) <> ''),
    experience varchar(500),
    housingconditions varchar(500),
    comment text
);

-- =========================================================
-- Messaging and donations
-- =========================================================

create table if not exists messages (
    messageid int generated by default as identity primary key,
    senderid int not null references users(userid),
    parentmessageid int references messages(messageid),
    subjectmessages varchar(200),
    messagetext text not null check (btrim(messagetext) <> ''),
    senddate timestamp not null default now(),
    isread boolean not null default false,
    recipientrole varchar(20) check (recipientrole in ('Manager', 'Admin') or recipientrole is null),
    recipientshelterid int references shelters(shelterid)
);

create table if not exists donations (
    donationid int generated by default as identity primary key,
    userid int not null references users(userid) on delete cascade,
    animalid int not null references animals(animalid) on delete cascade,
    amount numeric(10,2) not null check (amount > 0),
    donationdate timestamp not null default now(),
    comment varchar(255),
    isapproved boolean not null default false
);

create table if not exists news (
    newsid int generated by default as identity primary key,
    userid int not null references users(userid) on delete cascade,
    title varchar(100) not null check (btrim(title) <> ''),
    content text not null check (btrim(content) <> ''),
    postdate timestamp not null default now(),
    ispublished boolean not null default true,
    imagepath varchar(255),
    shelterid int references shelters(shelterid)
);

-- =========================================================
-- Meetings
